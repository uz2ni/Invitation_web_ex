package com.test.Builder.dto;

public class UserCertify {
    private int userCertifyId;
	private String userCertifyEmail;
	private String userCertifyNum;
	private int userCertifyStatus;
	
	public int getUserCertifyId() {
		return userCertifyId;
	}
	public void setUserCertifyId(int userCertifyId) {
		this.userCertifyId = userCertifyId;
	}
	public String getUserCertifyEmail() {
		return userCertifyEmail;
	}
	public void setUserCertifyEmail(String userCertifyEmail) {
		this.userCertifyEmail = userCertifyEmail;
	}
	public String getUserCertifyNum() {
		return userCertifyNum;
	}
	public void setUserCertifyNum(String userCertifyNum) {
		this.userCertifyNum = userCertifyNum;
	}
	public int getUserCertifyStatus() {
		return userCertifyStatus;
	}
	public void setUserCertifyStatus(int userCertifyStatus) {
		this.userCertifyStatus = userCertifyStatus;
	}
}
